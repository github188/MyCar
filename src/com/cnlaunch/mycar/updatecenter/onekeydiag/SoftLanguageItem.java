package com.cnlaunch.mycar.updatecenter.onekeydiag;

public class SoftLanguageItem
{
	int versionDetailId;
	String languageName;
	
	public SoftLanguageItem()
	{
	}
	public int getVersionDetailId()
	{
		return versionDetailId;
	}
	public void setVersionDetailId(int languageId)
	{
		this.versionDetailId = languageId;
	}
	public String getLanguageName()
	{
		return languageName;
	}
	public void setLanguageName(String languageName)
	{
		this.languageName = languageName;
	}
}
